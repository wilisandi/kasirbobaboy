private void TampilProduk() {
        DftTabelProduk = new DefaultTableModel();
        DftTabelProduk.addColumn("Id");
        DftTabelProduk.addColumn("Nama");
        DftTabelProduk.addColumn("Harga");

        tblProduk.setModel(DftTabelProduk);
        Connection conn = koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "SELECT * FROM produk";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTabelProduk.addRow(new Object[]{
                    res.getString("Id"),
                    res.getString("Nama"),
                    res.getString("Harga")});
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
