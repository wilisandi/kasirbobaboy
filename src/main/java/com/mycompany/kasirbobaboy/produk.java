    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int baris = tblProduk.getSelectedRow();
        if(baris==-1){
            JOptionPane.showMessageDialog(null, "Mohon Pilih Produk terlebih dahulu!", "Pesan", JOptionPane.WARNING_MESSAGE);
        }else{
            int id = Integer.parseInt(DftTabelProduk.getValueAt(baris, 0).toString());
            String nama = DftTabelProduk.getValueAt(baris, 1).toString();
            double harga = Double.parseDouble(DftTabelProduk.getValueAt(baris, 2).toString());
            editProduk frmEditProduk = new editProduk(id,nama,harga);
            frmEditProduk.show();   
            frmEditProduk.addWindowListener(new java.awt.event.WindowAdapter() {
                @Override
                public void windowClosed(java.awt.event.WindowEvent windowEvent) {
                    // Code to execute when frmAddProduk is closed
                    TampilProduk();
                    tblProduk.clearSelection();
                    btnEdit.setVisible(false);
        btnHapus.setVisible(false);
                }
            });
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int baris = tblProduk.getSelectedRow();
        if(baris==-1){
            JOptionPane.showMessageDialog(null, "Mohon Pilih Produk terlebih dahulu!", "Pesan", JOptionPane.WARNING_MESSAGE);
        }else{
            Connection conn = koneksi.getConnection();
            int confirm = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data tersebut?", "Konfirmasi", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (confirm == 0) {
                try {
                    java.sql.PreparedStatement stmt = conn.prepareStatement("delete from produk where Id ='" + DftTabelProduk.getValueAt(baris, 0).toString() + "'");
                    stmt.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                    TampilProduk();
                    tblProduk.clearSelection();
                    btnEdit.setVisible(false);
                    btnHapus.setVisible(false);
                } catch (SQLException e) {
                    System.out.println(e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

